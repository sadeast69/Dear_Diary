    private static AutomaticEventDefinition.Builder variants(
            AutomaticEventDefinition.Builder builder,
            String eventId,
            String titleFallback,
            String... textFallbacks
    ) {
        int variantCount = Math.max(textFallbacks.length, targetVariantCount(eventId));
        for (int index = 0; index < variantCount; index++) {
            int variant = index + 1;
            String fallback = textFallbacks[index % textFallbacks.length];
            builder.variant(Integer.toString(variant), title(eventId), text(eventId, variant), titleFallback, fallback);
        }

        return builder;
    }

    private static AutomaticEventDefinition.Builder zeroBasedVariants(
            AutomaticEventDefinition.Builder builder,
            String eventId,
            String titleFallback,
            String... textFallbacks
    ) {
        for (int index = 0; index < textFallbacks.length; index++) {
            builder.variant(Integer.toString(index), title(eventId), zeroBasedText(eventId, index), titleFallback, textFallbacks[index]);
        }

        return builder;
    }

