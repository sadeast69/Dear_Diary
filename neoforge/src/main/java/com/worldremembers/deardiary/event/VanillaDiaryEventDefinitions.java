    private static AutomaticEventDefinition.Builder once(
            String eventId,
            DiaryCategory category,
            DiaryImportance importance,
            String icon
    ) {
        return base(eventId, category, importance, icon)
                .triggerPolicy(TriggerPolicy.ONCE_PER_PLAYER);
    }

    private static AutomaticEventDefinition.Builder cooldown(
            String eventId,
            DiaryCategory category,
            DiaryImportance importance,
            String icon
    ) {
        return base(eventId, category, importance, icon)
                .triggerPolicy(TriggerPolicy.COOLDOWN)
                .cooldownMillis(DEATH_CAUSE_COOLDOWN_MILLIS);
    }

    private static AutomaticEventDefinition.Builder milestone(
            String eventId,
            String counter,
            int threshold,
            DiaryCategory category,
            DiaryImportance importance,
            String icon
    ) {
        return base(eventId, category, importance, icon)
                .triggerPolicy(TriggerPolicy.MILESTONE)
                .milestoneCounter(counter)
                .milestoneThreshold(threshold);
    }

    private static AutomaticEventDefinition.Builder base(
            String eventId,
            DiaryCategory category,
            DiaryImportance importance,
            String icon
    ) {
        return AutomaticEventDefinition.builder(eventId)
                .source("minecraft")
                .category(category)
                .importance(importance)
                .icon(icon)
                .includeLocation(true)
                .shareable(true);
    }

    private static String title(String eventId) {
        return baseKey(eventId) + ".title";
    }

    private static String text(String eventId, int variant) {
        return baseKey(eventId) + ".text." + variant;
    }

    private static String zeroBasedText(String eventId, int variant) {
        return baseKey(eventId) + ".text." + variant;
    }

    private static String baseKey(String eventId) {
        return "entry.dear_diary." + eventId.replace(':', '.');
    }
